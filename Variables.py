# Asks for user's first number
Num1 = int(input("What is your first number?"))
# Asks for user's second number
Num2 = int(input("What is yout second number?"))

#   Addition 
sum = Num1 + Num2
print ("Sum of {0} and {1} is {2}" .format(Num1, Num2, sum))
#   Subtraction 
Minus = Num1 - Num2
print ("Subtraction of {0} and {1} is {2}" .format(Num1, Num2, Minus))
#   Division 
Divided = Num1 / Num2
print ("Division of {0} and {1} is {2}" .format(Num1, Num2, Divided))
#   Multiplayer 
Multiplied = Num1 * Num2
print ("Multiplication of {0} and {1} is {2}" .format(Num1, Num2, Multiplied))




